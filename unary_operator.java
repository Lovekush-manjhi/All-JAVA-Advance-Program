public class unarry_operator {
    public static void main(String args[]) {

        System.out.print("unary operator : \n ");
        int a=10;
        int b=45;
        //Increment Operator 
      System.out.print("\nPre Increment Operator : " + (++a));
      System.out.print("\nPost Increment :"+ (b++));
      System.out.print("\nSecond time Post Increment : "+ (b++));

      //Decreement Operator : 
      int c=5;
      int d=15;
      System.out.print("\n\nPre decrement Operator : " + (--c));
      System.out.print("\nPost decrement :"+ (d--));
      System.out.print("\nSecond time Post decrement : "+ (d--));

    }
}
/*Unary Operator   :  one operand and two Operator */