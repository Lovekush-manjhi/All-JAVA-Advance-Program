/*
In Java, operators are symbols that perform various operations on variables and values. They are used to manipulate data and perform calculations in expressions. Java supports a wide range of operators, which can be broadly categorized into the following groups:

Arithmetic Operators:

+: Addition
-: Subtraction
*: Multiplication
/: Division
%: Modulus (remainder) */

// Example:
public class Arithmetic_operator_in_java {
    public static void main(String[] args) {
        int n1=45;
        int n2=10;

        System.out.println("Addition of "+ n1 + " and "+ n2 +" is : "+ (n1+n2));
        System.out.println("Subtraction of "+ n1 + " and "+ n2 +" is : "+ (n1-n2));
        System.out.println("Multiplication of "+ n1 + " and "+ n2 +" is : "+ (n1*n2));
        System.out.println("Division of "+ n1 + " and "+ n2 +" is : "+ (n1/n2));
        System.out.println("Modules of "+ n1 + " and "+ n2 +" is : "+ (n1/n2));
 
    }
}


